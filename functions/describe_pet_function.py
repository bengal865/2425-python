def describe_pet(pet_type, pet_name):
	'''Function that describes someone's pet.'''
	print( 'I have a {}.  My pet\'s name is {}.'.format( pet_type, pet_name ) )
		

# Call the describe_pet function
describe_pet('cat', 'Sepp')	

describe_pet('rabbit', 'Bugs')

describe_pet('hamster', 'Oliver')
