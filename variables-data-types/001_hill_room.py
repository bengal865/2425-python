# Area Calculation

# Stores length and width of room
length = 12.5
width = 16.7

# Calculates area and perimter, rounding the area
area = round(length * width, 1)
perimeter = ( length * 2 ) + ( width * 2 )

# Display area and perimeter
print(f"The area of your room is: {area} (square meters")
print(f"The perimeter of your room is: {perimeter} (meters)")
