# Script to calculate the perimeter and area of a rectangular room

# w represents the room's width
# l represents the room's length

# Perimeter = 2w + 2l
# Area = l * w

# Specify room dimensions (in meters)

length = 16.7
width = 12.5

# Formula for calculating the perimeter of the room

perimeter = (2 * length) + (2 * width) 

# Formula for calculating the area of the room
# Answer will be in square meters

area = length * width

