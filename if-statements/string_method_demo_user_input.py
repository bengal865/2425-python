# Prompt user for first name
first_name = input('Please enter your first name:\n')

if first_name == 'eric':
    print(f'Good morning, {first_name}! How are you today?')

elif first_name == 'carli':
    print(f'Nice to see you again, {first_name}!')

elif first_name == 'maddox':
    print(f'Long time, no see {first_name}!')
    
else:
    print(f'We haven\'t met before, have we, {first_name}?')

